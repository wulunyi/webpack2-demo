export function encodeHTML(str) {
    if (typeof str == 'string') {
        var ar = ['&', '&amp;', '<', '&lt;', '>', '&gt;', '"', '&quot;'];
        for (var i = 0; i < ar.length; i += 2) {
            str = str.replace(new RegExp(ar[i], 'g'), ar[1 + i]);
        }
        return str;
    }
    return str;
}

